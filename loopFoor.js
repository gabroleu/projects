//o loop for é usado quando você sabe exatamente quantas vezes deseja repetir o cógigo.

for (let indice = 0; indice < 20; indice++) {
    console.log("Numero", indice)
}

for (let i = 0; i < 5; i++){
    console.log (`Numero ${i}`);
}

// let i = 0: inicializa o contador
// i < 5: define a condição para continuar o loop
// i++: incrementa o valor de i a cada iteração